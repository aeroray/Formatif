const dicts: Record<Lang, Dict> = { en, zh }

export function translate(
  lang: Lang,
  key: string,
  vars?: Record<string, string | number>
): string {
  let s = dicts[lang]?.[key] ?? en[key] ?? key
  if (vars) {
    for (const k of Object.keys(vars)) {
      s = s.replace(`{${k}}`, String(vars[k]))
    }
  }
  return s
}

export type TFn = (key: string, vars?: Record<string, string | number>) => string

/** Reactive translator hook — re-renders when the language changes. */
export function useT(): TFn {
  const lang = useSettingsStore((s) => s.lang)
  return (key, vars) => translate(lang, key, vars)
}

/** Imperative translator for callbacks/toasts (reads the current language). */
export function tx(key: string, vars?: Record<string, string | number>): string {
  return translate(useSettingsStore.getState().lang, key, vars)
}
