/** Pick a language from the system locale (navigator.language). */
export function detectLang(): Lang {
  const l =
    typeof navigator !== "undefined" && navigator.language
      ? navigator.language.toLowerCase()
      : "en"
  return l.startsWith("zh") ? "zh" : "en"
}

